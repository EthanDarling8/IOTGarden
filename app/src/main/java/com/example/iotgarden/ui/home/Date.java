package com.example.iotgarden.ui.home;

public class Date {
    public String year;
    public String month;
    public String day;
    public String hour;

    public Date() {

    }

    public Date(String year, String month, String day, String hour) {
        this.year = year;
        this.month = month;
        this.day = day;
        this.hour = hour;
    }
}
